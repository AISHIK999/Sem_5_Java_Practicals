public class W1_TypeCasting {
    //JAVA program that works with type casting
    public static void main(String[] args) {
        double a = 12.345;
        int b = (int)a;     // Variable 'a' of double type has been cast to  integer type
        System.out.println("Double type:" + a);
        System.out.println("Integer type:" + b);
    }
}
